package com.tathao.shop.bean;

public class ProductCatalogs {

	private int id;
	private String name;
	
	public ProductCatalogs(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
